class Simple_Calc:

    def add(self, val_1, val_2):
        return val_1 + val_2

    def subtract(self, val_1, val_2):
        return val_1 - val_2

    def multiply(self, val_1, val_2):
        return val_1 * val_2

    def divide(self, val_1, val_2):
        return val_1 / val_2

